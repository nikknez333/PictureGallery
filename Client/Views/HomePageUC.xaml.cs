    /// <summary>
    /// Interaction logic for HomePageUC.xaml
    /// </summary>
    public partial class HomePageUC : UserControl
    {
        public HomePageUC()
        {
            InitializeComponent();
        }

        private void ButtonOpenMenu_Click(object sender, RoutedEventArgs e)
        {
            ButtonOpenMenu.Visibility = Visibility.Collapsed;
            ButtonCloseMenu.Visibility = Visibility.Visible;
            TxtServices.Visibility = Visibility.Collapsed;
            TxtHomeMenu.Visibility = Visibility.Visible;
        }

        private void ButtonCloseMenu_Click(object sender, RoutedEventArgs e)
        {
            ButtonOpenMenu.Visibility = Visibility.Visible;
            ButtonCloseMenu.Visibility = Visibility.Collapsed;
            TxtServices.Visibility = Visibility.Visible;
            TxtHomeMenu.Visibility = Visibility.Collapsed;
        }
    }
}
